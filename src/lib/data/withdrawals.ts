/**
 * Hybrid Withdrawals Loader
 *
 * Strategy:
 * 1. Fetch cached data from Supabase (fast, ~50ms)
 * 2. In parallel, fetch delta from Match-Trade API (since last_synced_at)
 * 3. Merge with timestamp-based deduplication
 * 4. Return combined result
 *
 * Fallback:
 * - If API fails/times out, return cached data only with warning
 */
export async function getWithdrawalsHybrid(
  options: GetWithdrawalsOptions = {}
): Promise<HybridWithdrawalsResult> {
  const {
    limit = DEFAULT_LIMIT,
    status = 'ALL',
    apiTimeout = DEFAULT_API_TIMEOUT,
  } = options;

  const startTime = Date.now();
  const supabase = createServerClient();

  // Get last sync timestamp for delta query
  const lastSyncedAt = await getLastSyncedAt('withdrawals');
  const syncTimestamp = getSyncTimestamp(lastSyncedAt, DELTA_SYNC_BUFFER_MS);

  // Parallel fetch: DB (cached with pagination) + API (delta)
  const [dbResult, deltaResult] = await Promise.allSettled([
    // DB: Fetch all records with pagination (Supabase limits to 1000 per query)
    fetchAllFromSupabase(supabase, { status, limit }),

    // Delta: New/updated records from API with timeout
    withTimeout(
      fetchWithdrawalsFromMatchTrade({
        from: syncTimestamp,
        size: 100, // Fetch more to catch all deltas
        sort: 'created,desc',
      }),
      apiTimeout
    ),
  ]);

  // Process DB result
  let dbData: Withdrawal[] = [];
  if (dbResult.status === 'fulfilled') {
    dbData = dbResult.value || [];
  } else {
    console.error('[Withdrawals] DB fetch failed:', dbResult.reason);
    throw new Error('Database unavailable');
  }

  // Process API delta result
  let deltaData: Withdrawal[] = [];
  let apiAvailable = true;
  let warning: string | undefined;

  if (deltaResult.status === 'fulfilled') {
    // Transform API records to Withdrawal format
    deltaData = (deltaResult.value.content || []).map(transformToWithdrawal);
  } else {
    apiAvailable = false;
    warning = `API unavailable: ${deltaResult.reason?.message || 'Unknown error'}. Showing cached data.`;
    console.warn('[Withdrawals] API delta fetch failed:', deltaResult.reason);
  }

  // Merge and sort
  const { data: mergedData, stats: mergeStats } = mergeAndSortWithdrawals(
    dbData,
    deltaData
  );

  // Apply limit after merge (delta might add new records)
  const limitedData = limit > 0 ? mergedData.slice(0, limit) : mergedData;

  // Transform to UI format
  const withdrawalEvents = limitedData.map(transformToWithdrawalEvent);

  // Calculate stats from merged data
  const paymentStats = calculateStats(mergedData);

  const duration = Date.now() - startTime;

  return {
    withdrawals: withdrawalEvents,
    stats: paymentStats,
    metadata: {
      source: !apiAvailable ? 'cache-only' : deltaData.length > 0 ? 'hybrid' : 'cache-only',
      dbCount: mergeStats.dbCount,
      deltaCount: mergeStats.deltaCount,
      totalCount: mergeStats.mergedCount,
      lastSyncedAt: lastSyncedAt.toISOString(),
      fetchedAt: new Date().toISOString(),
      duration,
      apiAvailable,
      warning,
    },
  };
}

