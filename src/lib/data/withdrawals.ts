/**
 * Transform Match-Trade API record to Supabase Withdrawal format
 */
export function transformToWithdrawal(record: MatchTradePaymentRecord): Withdrawal {
  const mappedStatus = mapPaymentStatus(
    record.paymentRequestInfo.financialDetails.status
  ) as WithdrawalMappedStatus;

  return {
    id: '', // Will be generated by Supabase
    uuid: record.uuid,
    account_uuid: record.accountInfo.accountUuid,
    account_email: record.accountInfo.email || null,
    account_name: record.accountInfo.personalDetails?.firstname || null,
    account_surname: record.accountInfo.personalDetails?.lastname || null,
    amount: record.paymentRequestInfo.financialDetails.amount || 0,
    net_amount: record.paymentRequestInfo.financialDetails.netAmount || null,
    currency: record.paymentRequestInfo.financialDetails.currency || 'USD',
    status: record.paymentRequestInfo.financialDetails.status,
    mapped_status: mappedStatus,
    payment_gateway_uuid: record.paymentRequestInfo.paymentGatewayDetails?.uuid || null,
    payment_gateway_name: record.paymentRequestInfo.paymentGatewayDetails?.name || null,
    wallet_address: record.paymentRequestInfo.additionalInfo?.walletAddress || null,
    reference: record.paymentRequestInfo.additionalInfo?.reference || null,
    payment_id: record.paymentRequestInfo.additionalInfo?.paymentId || null,
    partner_id: record.partnerId || null,
    created_at: record.created,
    updated_at: record.created, // Use created as initial updated_at
    synced_at: new Date().toISOString(),
    raw_data: record as unknown as Withdrawal['raw_data'],
  };
}

/**
 * Transform Supabase Withdrawal to UI WithdrawalEvent format
 */
export function transformToWithdrawalEvent(withdrawal: Withdrawal): WithdrawalEvent {
  return {
    uuid: withdrawal.uuid,
    timestamp: withdrawal.created_at,
    accountInfo: {
      uuid: withdrawal.account_uuid,
      email: withdrawal.account_email || '',
      name: withdrawal.account_name || '',
      surname: withdrawal.account_surname || '',
    },
    status: withdrawal.mapped_status,
    amount: withdrawal.amount,
    currency: withdrawal.currency,
    method: withdrawal.payment_gateway_name || undefined,
  };
}

/**
 * Calculate statistics from withdrawals
 */
export function calculateStats(withdrawals: Withdrawal[]): PaymentStats {
  const stats: PaymentStats = {
    totalAmount: 0,
    totalCount: withdrawals.length,
    pendingCount: 0,
    approvedCount: 0,
    rejectedCount: 0,
    pendingAmount: 0,
    approvedAmount: 0,
    rejectedAmount: 0,
  };

  for (const w of withdrawals) {
    stats.totalAmount += w.amount;

    switch (w.mapped_status) {
      case 'PENDING':
        stats.pendingCount++;
        stats.pendingAmount += w.amount;
        break;
      case 'APPROVED':
        stats.approvedCount++;
        stats.approvedAmount += w.amount;
        break;
      case 'REJECTED':
        stats.rejectedCount++;
        stats.rejectedAmount += w.amount;
        break;
    }
  }

  return stats;
}

