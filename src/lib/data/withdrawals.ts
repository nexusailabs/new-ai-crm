/**
 * Get withdrawals from cache only (no API call)
 * Use when you need guaranteed fast response
 */
export async function getWithdrawalsFromCache(
  options: Omit<GetWithdrawalsOptions, 'forceRefresh' | 'apiTimeout'> = {}
): Promise<HybridWithdrawalsResult> {
  const { limit = DEFAULT_LIMIT, status = 'ALL' } = options;
  const startTime = Date.now();
  const supabase = createServerClient();

  // Use pagination helper to fetch all records (Supabase limits to 1000 per query)
  const withdrawals = await fetchAllFromSupabase(supabase, { status, limit });

  const duration = Date.now() - startTime;
  const lastSyncedAt = await getLastSyncedAt('withdrawals');

  return {
    withdrawals: withdrawals.map(transformToWithdrawalEvent),
    stats: calculateStats(withdrawals),
    metadata: {
      source: 'cache-only',
      dbCount: withdrawals.length,
      deltaCount: 0,
      totalCount: withdrawals.length,
      lastSyncedAt: lastSyncedAt.toISOString(),
      fetchedAt: new Date().toISOString(),
      duration,
      apiAvailable: false,
      warning: 'Serving cached data only',
    },
  };
}

// ============================================================================
// API-Only Loader (Initial Sync)
// ============================================================================

/**
 * Get withdrawals directly from API (no cache)
 * Use for initial sync or when cache is empty
 */
export async function getWithdrawalsFromAPI(
  options: { size?: number } = {}
): Promise<MatchTradePaymentRecord[]> {
  const { size = 100 } = options;

  const response = await fetchWithdrawalsFromMatchTrade({
    size,
    sort: 'created,desc',
  });

  return response.content || [];
}
