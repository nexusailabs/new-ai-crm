// ============================================================================
// Types
// ============================================================================

export interface HybridWithdrawalsResult {
  withdrawals: WithdrawalEvent[];
  stats: PaymentStats;
  metadata: {
    source: 'hybrid' | 'cache-only' | 'api-only';
    dbCount: number;
    deltaCount: number;
    totalCount: number;
    lastSyncedAt: string;
    fetchedAt: string;
    duration: number;
    apiAvailable: boolean;
    warning?: string;
  };
}

export interface GetWithdrawalsOptions {
  limit?: number;
  status?: 'PENDING' | 'APPROVED' | 'REJECTED' | 'ALL';
  forceRefresh?: boolean;
  apiTimeout?: number;
}

// ============================================================================
// Configuration
// ============================================================================

const DEFAULT_LIMIT = 10000;  // Load all data for scrolling
const DEFAULT_API_TIMEOUT = 5000; // 5 seconds for larger dataset
const DELTA_SYNC_BUFFER_MS = 1000; // 1 second buffer for clock drift
const SUPABASE_PAGE_SIZE = 1000;   // Supabase max rows per query

// ============================================================================
// Supabase Pagination Helper
// ============================================================================

/**
 * Fetch all records from Supabase with pagination
 * Supabase limits to 1000 rows per query, so we fetch in batches
 */
async function fetchAllFromSupabase(
  supabase: ReturnType<typeof createServerClient>,
  options: {
    status: 'PENDING' | 'APPROVED' | 'REJECTED' | 'ALL';
    limit: number;
  }
): Promise<Withdrawal[]> {
  const { status, limit } = options;
  const allRecords: Withdrawal[] = [];
  let offset = 0;

  while (allRecords.length < limit) {
    const pageSize = Math.min(SUPABASE_PAGE_SIZE, limit - allRecords.length);

    let query = supabase
      .from('withdrawals')
      .select('*')
      .order('created_at', { ascending: false })
      .range(offset, offset + pageSize - 1);

    if (status !== 'ALL') {
      query = query.eq('mapped_status', status);
    }

    const { data, error } = await query;

    if (error) {
      console.error('[Withdrawals] Supabase query error:', error);
      throw error;
    }

    if (!data || data.length === 0) {
      break; // No more records
    }

    allRecords.push(...data);
    offset += pageSize;

    // If we got less than page size, we've reached the end
    if (data.length < pageSize) {
      break;
    }
  }

  console.log(`[Withdrawals] Fetched ${allRecords.length} records from Supabase (${Math.ceil(offset / SUPABASE_PAGE_SIZE)} pages)`);
  return allRecords;
}

